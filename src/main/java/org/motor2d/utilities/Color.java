package org.motor2d.utilities;

/**
 * Clase que centraliza todos los colores utilizados en la aplicación.
 * Facilita la consistencia visual y permite cambios globales de temas.
 */
public class Color {

    // ============================ INDEX ============================ //

    // COLORES PRINCIPALES
    /** Fondo oscuro principal de la aplicación */
    public static final java.awt.Color BACKGROUND = new java.awt.Color(30, 30, 30);

    // COLORES DE BOTONES //
    /** Color por defecto de los botones */
    public static final java.awt.Color BUTTON_DEFAULT = new java.awt.Color(50, 50, 50);

    /** Color del botón al pasar el ratón (hover) */
    public static final java.awt.Color BUTTON_HOVER = new java.awt.Color(70, 70, 70);

    /** Color del botón al presionar */
    public static final java.awt.Color BUTTON_PRESSED = new java.awt.Color(40, 40, 40);

    // ====================  ====================


}